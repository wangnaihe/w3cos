//! ESM bundle -> Rust module skeleton code generation.
//!
//! Takes a flattened `EsmBundle` (see `esm_resolver`) and emits a Rust module
//! skeleton: each ESM module becomes a `mod`, each top-level `class` becomes a
//! `struct` + `impl`, each top-level `function` becomes an `fn`. Cross-module
//! bindings are emitted as `use` re-exports so the bundled names line up.
//!
//! `generate_skeleton` produces `todo!()` placeholders.
//! `generate_with_bodies` reads source files and lowers JS function bodies to Rust.

use crate::esm_lowering::{ClassScope, LowerCtx, is_super_call_stmt, sanitize_ident};
use crate::esm_resolver::{BundledModule, BundledSymbol, EsmBundle, SymbolKind};
use std::collections::HashSet;
use std::path::Path;
use swc_common::{FileName, SourceMap, sync::Lrc};
use swc_ecma_ast::*;
use swc_ecma_parser::{EsSyntax, Parser, StringInput, Syntax, TsSyntax, lexer::Lexer};

fn json_value_expr(value: &serde_json::Value) -> String {
    match value {
        serde_json::Value::Null => "w3cos_core::Value::Null".to_string(),
        serde_json::Value::Bool(value) => format!("w3cos_core::Value::Bool({value})"),
        serde_json::Value::Number(value) => format!(
            "w3cos_core::Value::Number({})",
            value.as_f64().unwrap_or_default()
        ),
        serde_json::Value::String(value) => {
            format!("w3cos_core::Value::from({value:?})")
        }
        serde_json::Value::Array(values) => format!(
            "w3cos_core::Value::array(vec![{}])",
            values
                .iter()
                .map(json_value_expr)
                .collect::<Vec<_>>()
                .join(", ")
        ),
        serde_json::Value::Object(values) => format!(
            "w3cos_core::Value::object(::std::collections::HashMap::from([{}]))",
            values
                .iter()
                .map(|(key, value)| format!("({key:?}.to_string(), {})", json_value_expr(value)))
                .collect::<Vec<_>>()
                .join(", ")
        ),
    }
}

fn emit_literal_imports(module: &BundledModule) -> String {
    module
        .literal_imports
        .iter()
        .map(|(local, source)| {
            let value = serde_json::from_str(source)
                .map(|value| json_value_expr(&value))
                .unwrap_or_else(|_| "w3cos_core::Value::Undefined".to_string());
            format!(
                "    pub fn {}_get() -> w3cos_core::Value {{ {value} }}\n",
                sanitize_ident(local)
            )
        })
        .collect()
}

/// Generate a Rust module skeleton from a fully built ESM bundle.
pub fn generate_skeleton(bundle: &EsmBundle) -> String {
    let mut out = String::new();
    out.push_str("// Generated by w3cos esm_codegen (structural skeleton)\n");
    out.push_str(&format!("// bundle entry: {}\n", bundle.entry.display()));
    out.push_str(&format!(
        "// modules: {}, symbols: {}\n\n",
        bundle.modules.len(),
        bundle.symbols.len()
    ));

    for module in &bundle.modules {
        out.push_str(&generate_module(bundle, module));
        out.push('\n');
    }

    out
}

fn generate_module(bundle: &EsmBundle, module: &BundledModule) -> String {
    let mut out = String::new();
    out.push_str(&format!(
        "/// ESM module: {}\nmod {} {{\n",
        module.path.display(),
        module.namespace
    ));
    for (local, host_path) in &module.host_imports {
        out.push_str(&format!(
            "    pub fn {}(__args: Vec<w3cos_core::Value>) -> w3cos_core::Value {{ w3cos_core::host_modules::call({host_path:?}, __args) }}\n",
            sanitize_ident(local),
        ));
    }
    out.push_str(&emit_literal_imports(module));
    for ns_import in &module.namespace_imports {
        out.push_str(&format!(
            "    /// namespace import {} ({} exports)\n    pub fn {}() {{ todo!(\"namespace object\") }}\n",
            ns_import.local,
            ns_import.exports.len(),
            namespace_fn_name(module, &ns_import.local)
        ));
    }

    // Emit `use` aliases for imported bindings so local names resolve to the
    // bundled definition living in another module's namespace.
    for (local, bundled) in &module.local_to_bundled {
        // Only emit a use for cross-module references (bundled name not owned here).
        let owned = bundle
            .symbols
            .iter()
            .any(|s| s.bundled_name == *bundled && s.module == module.path);
        if owned {
            continue;
        }
        if let Some(owner) = bundle.modules.iter().find(|m| {
            bundle
                .symbols
                .iter()
                .any(|s| s.bundled_name == *bundled && s.module == m.path)
        }) {
            out.push_str(&format!(
                "    pub use super::{}::{} as {};\n",
                owner.namespace,
                bundled,
                sanitize_ident(local)
            ));
        }
    }

    // Emit the module's own definitions.
    for sym in bundle.symbols.iter().filter(|s| s.module == module.path) {
        match sym.kind {
            SymbolKind::Class => {
                out.push_str(&format!(
                    "\n    /// class {} (from ESM)\n    pub struct {} {{}}\n",
                    sym.original_name, sym.bundled_name
                ));
                out.push_str(&format!(
                    "    impl {} {{\n        pub fn new() -> Self {{ Self {{}} }}\n    }}\n",
                    sym.bundled_name
                ));
                // Alias so the original name is usable within this module.
                let alias = sanitize_ident(&sym.original_name);
                if alias != sym.bundled_name {
                    out.push_str(&format!(
                        "    pub use self::{} as {};\n",
                        sym.bundled_name, alias
                    ));
                }
            }
            SymbolKind::Function => {
                out.push_str(&format!(
                    "\n    /// function {} (from ESM)\n    pub fn {}() {{ todo!(\"lower ESM body: {}\") }}\n",
                    sym.original_name, sym.bundled_name, sym.original_name
                ));
                let alias = sanitize_ident(&sym.original_name);
                if alias != sym.bundled_name {
                    out.push_str(&format!(
                        "    pub use self::{} as {};\n",
                        sym.bundled_name, alias
                    ));
                }
            }
            SymbolKind::Variable => {
                out.push_str(&format!(
                    "\n    // variable {} (from ESM) — lowering pending\n",
                    sym.original_name
                ));
            }
        }
    }

    out.push_str("}\n");
    out
}

/// Generate Rust module code with real function bodies lowered from JS AST.
pub fn generate_with_bodies(bundle: &EsmBundle) -> String {
    generate_with_bodies_and_css(bundle, &[])
}

/// Generate Rust module code, additionally baking collected ESM CSS rules
/// into a `register_styles()` function that `run_entry()` calls FIRST —
/// stylesheet rules must be in the registry before app code builds the DOM.
pub fn generate_with_bodies_and_css(
    bundle: &EsmBundle,
    css_rules: &[crate::esm_css::CollectedRule],
) -> String {
    let mut out = String::new();
    // Generated bundles intentionally use source-shaped JS identifiers and
    // mutable temporaries; suppressing those mechanical Rust warnings keeps
    // Monaco-scale builds from emitting tens of thousands of diagnostics.
    out.push_str("#![allow(warnings)]\n// Generated by w3cos esm_codegen (with lowered bodies)\n");
    out.push_str(&format!("// bundle entry: {}\n", bundle.entry.display()));
    out.push_str(&format!(
        "// modules: {}, symbols: {}\n\n",
        bundle.modules.len(),
        bundle.symbols.len()
    ));

    let mut parse_cache = ModuleParseCache::default();
    for module in &bundle.modules {
        out.push_str(&generate_module_with_bodies(
            bundle,
            module,
            &mut parse_cache,
        ));
        out.push('\n');
    }

    out.push_str(&emit_register_styles(css_rules));

    // ESM evaluation order: every module's top-level statements run once, in
    // bundle (topological) order, before the entry's main. Circular imports
    // may still observe Undefined early (see emit_module_init's NOTE).
    let init_calls = if bundle.modules.is_empty() {
        String::new()
    } else {
        format!(
            " {}",
            bundle
                .modules
                .iter()
                .map(|module| format!("{}::{}__init();", module.namespace, module.namespace))
                .collect::<Vec<_>>()
                .join(" ")
        )
    };

    if let Some(symbol) = bundle
        .symbols
        .iter()
        .find(|symbol| symbol.module == bundle.entry && symbol.original_name == "main")
        && let Some(module) = bundle
            .modules
            .iter()
            .find(|module| module.path == bundle.entry)
    {
        out.push_str(&format!(
            "pub fn run_entry() -> w3cos_core::Value {{ register_styles();{init_calls} {}::{}(vec![]) }}\n",
            module.namespace, symbol.bundled_name
        ));
    } else {
        out.push_str(&format!(
            "pub fn run_entry() -> w3cos_core::Value {{ register_styles();{init_calls} w3cos_core::Value::Undefined }}\n"
        ));
    }

    out
}

/// Emit the stylesheet registration function (no-op when no CSS was collected).
fn emit_register_styles(css_rules: &[crate::esm_css::CollectedRule]) -> String {
    let mut out = String::from(
        "\n/// Stylesheet rules collected from ESM `.css` imports (baked in at compile time).\npub fn register_styles() {\n",
    );
    for rule in css_rules {
        let declarations = rule
            .declarations
            .iter()
            .map(|(prop, value)| format!("({prop:?}, {value:?})"))
            .collect::<Vec<_>>()
            .join(", ");
        out.push_str(&format!(
            "    w3cos_runtime::stylesheet::register_rule({:?}, &[{declarations}]);\n",
            rule.selector
        ));
    }
    out.push_str("}\n\n");
    out
}

fn generate_module_with_bodies(
    bundle: &EsmBundle,
    module: &BundledModule,
    parse_cache: &mut ModuleParseCache,
) -> String {
    let mut out = String::new();
    out.push_str(&format!(
        "/// ESM module: {}\nmod {} {{\n",
        module.path.display(),
        module.namespace
    ));
    out.push_str(
        "    #[allow(unused_imports)]\n    use w3cos_core::{Array, Error, ErrorValue, Map, Math, Object, RangeError, ResizeObserver, Set, console, parseFloat, parseInt};\n",
    );
    // Android's pthread-key limit is small. Emitting one `thread_local!` for
    // every lowered JS binding exhausts it before `android_main` can start, so
    // keep all per-thread ESM state in two module-level registries.
    out.push_str(
        "    std::thread_local! {\n        static __W3COS_ESM_VALUES: std::cell::RefCell<::std::collections::HashMap<&'static str, w3cos_core::Value>> = std::cell::RefCell::new(::std::collections::HashMap::new());\n        static __W3COS_ESM_FLAGS: std::cell::RefCell<::std::collections::HashSet<&'static str>> = std::cell::RefCell::new(::std::collections::HashSet::new());\n    }\n    fn __w3cos_esm_get(key: &'static str) -> Option<w3cos_core::Value> { __W3COS_ESM_VALUES.with(|values| values.borrow().get(key).cloned()) }\n    fn __w3cos_esm_get_or_init(key: &'static str, init: impl FnOnce() -> w3cos_core::Value) -> w3cos_core::Value { if let Some(value) = __w3cos_esm_get(key) { return value; } let value = init(); __w3cos_esm_set(key, value.clone()); value }\n    fn __w3cos_esm_set(key: &'static str, value: w3cos_core::Value) { __W3COS_ESM_VALUES.with(|values| { values.borrow_mut().insert(key, value); }); }\n    fn __w3cos_esm_flag_replace(key: &'static str, value: bool) -> bool { __W3COS_ESM_FLAGS.with(|flags| { let mut flags = flags.borrow_mut(); let previous = flags.contains(key); if value { flags.insert(key); } else { flags.remove(key); } previous }) }\n",
    );
    let namespace_names: HashSet<String> = module
        .namespace_imports
        .iter()
        .map(|ns| sanitize_ident(&ns.local))
        .collect();
    for (local, host_path) in &module.host_imports {
        out.push_str(&format!(
            "    pub fn {}(__args: Vec<w3cos_core::Value>) -> w3cos_core::Value {{ w3cos_core::host_modules::call({host_path:?}, __args) }}\n",
            sanitize_ident(local),
        ));
    }
    out.push_str(&emit_literal_imports(module));

    // Emit cross-module use aliases (same as skeleton), except variable
    // symbols: those are exposed through `{bundled}_get`/`{bundled}_set`
    // accessor fns, so the aliases must target the accessors.
    for (local, bundled) in &module.local_to_bundled {
        let sym = bundle.symbols.iter().find(|s| s.bundled_name == *bundled);
        let owned = sym.is_some_and(|s| s.module == module.path);
        if owned {
            continue;
        }
        let Some(sym) = sym else { continue };
        if let Some(owner) = bundle.modules.iter().find(|m| m.path == sym.module) {
            if sym.kind == SymbolKind::Variable
                && variable_has_get_accessors(bundle, sym, parse_cache)
            {
                // Both the bundled accessor name (used by the lowering) and
                // the local alias (used by re-exports) must be in scope.
                out.push_str(&format!(
                    "    pub use super::{}::{}_get;\n    pub use super::{}::{}_get as {};\n    pub use super::{}::{}_set;\n",
                    owner.namespace,
                    bundled,
                    owner.namespace,
                    bundled,
                    sanitize_ident(local),
                    owner.namespace,
                    bundled
                ));
            } else if sym.kind == SymbolKind::Function {
                let local = sanitize_ident(local);
                out.push_str(&format!(
                    "    pub use super::{}::{} as {};\n    pub use super::{}::{}_value;\n    pub use super::{}::{}_value as {}_value;\n",
                    owner.namespace,
                    bundled,
                    local,
                    owner.namespace,
                    bundled,
                    owner.namespace,
                    bundled,
                    local,
                ));
            } else {
                out.push_str(&format!(
                    "    pub use super::{}::{} as {};\n",
                    owner.namespace,
                    bundled,
                    sanitize_ident(local)
                ));
            }
        }
    }

    // Emit `import * as ns` namespace accessors: a lazily built object whose
    // properties are the target module's exports.
    for ns_import in &module.namespace_imports {
        out.push_str(&emit_namespace_import(
            bundle,
            module,
            ns_import,
            parse_cache,
        ));
    }

    // Parse the module's source to get function bodies (dropped after this
    // module's emission; only the lightweight shape index is cached).
    let parsed_module = parse_module_items(&module.path);
    let functions = parsed_module.items;
    let init_items = parsed_module.init;
    let mut value_bindings: HashSet<String> = functions
        .iter()
        .filter_map(|item| match item {
            TopLevelItem::Variable { name, .. } => Some(sanitize_ident(name)),
            _ => None,
        })
        .collect();
    value_bindings.extend(
        module
            .literal_imports
            .iter()
            .map(|(local, _)| sanitize_ident(local)),
    );
    // Variables emitted in `{bundled}_get`/`_set` accessor form read through
    // the accessor — including own ambient/stubbed variables and aliases to
    // non-local targets (see variable_has_get_accessors).
    for sym in bundle
        .symbols
        .iter()
        .filter(|s| s.module == module.path && s.kind == SymbolKind::Variable)
    {
        if variable_has_get_accessors(bundle, sym, parse_cache) {
            value_bindings.insert(sanitize_ident(&sym.original_name));
        }
    }
    // Imported variable bindings also read through the owner's `{bundled}_get`
    // accessor (and write through `{bundled}_set`), so they count as value
    // bindings for the lowering (see LowerCtx::resolve_name).
    for (local, bundled) in &module.local_to_bundled {
        let Some(sym) = bundle.symbols.iter().find(|s| s.bundled_name == *bundled) else {
            continue;
        };
        if sym.module != module.path
            && sym.kind == SymbolKind::Variable
            && variable_has_get_accessors(bundle, sym, parse_cache)
        {
            value_bindings.insert(sanitize_ident(local));
        }
    }
    let class_names = module_class_names(bundle, module);

    // Emit definitions with bodies.
    for sym in bundle.symbols.iter().filter(|s| s.module == module.path) {
        match sym.kind {
            SymbolKind::Class => {
                let renames = lowering_names(module);
                let class_ast = find_class(&functions, &sym.original_name);
                out.push_str(&emit_class(
                    sym,
                    class_ast,
                    &renames,
                    &value_bindings,
                    &class_names,
                    &namespace_names,
                ));
                let alias = sanitize_ident(&sym.original_name);
                if alias != sym.bundled_name {
                    out.push_str(&format!(
                        "    pub use self::{} as {};\n",
                        sym.bundled_name, alias
                    ));
                }
            }
            SymbolKind::Function => {
                let renames = lowering_names(module);
                let body = find_function(
                    &functions,
                    &sym.original_name,
                    &renames,
                    &value_bindings,
                    &class_names,
                    &namespace_names,
                    false,
                )
                .unwrap_or_else(|| {
                    format!("        todo!(\"body not found: {}\");", sym.original_name)
                });
                let callable_body = find_function(
                    &functions,
                    &sym.original_name,
                    &renames,
                    &value_bindings,
                    &class_names,
                    &namespace_names,
                    true,
                )
                .unwrap_or_else(|| body.clone());
                out.push_str(&format!(
                    "\n    /// function {} (from ESM)\n    pub fn {}(__args: Vec<w3cos_core::Value>) -> w3cos_core::Value {{\n{}\n        w3cos_core::Value::Undefined\n    }}\n    fn {}_call(__this: w3cos_core::Value, __args: Vec<w3cos_core::Value>) -> w3cos_core::Value {{\n{}\n        w3cos_core::Value::Undefined\n    }}\n",
                    sym.original_name, sym.bundled_name, body, sym.bundled_name, callable_body
                ));
                out.push_str(&format!(
                    "    pub fn {}_value() -> w3cos_core::Value {{\n        __w3cos_esm_get_or_init({:?}, || w3cos_core::Value::function(move |__this, __args| {}_call(__this, __args)))\n    }}\n",
                    sym.bundled_name,
                    format!("{}:value", sym.bundled_name),
                    sym.bundled_name,
                ));
                let alias = sanitize_ident(&sym.original_name);
                if alias != sym.bundled_name {
                    out.push_str(&format!(
                        "    pub use self::{} as {};\n    pub use self::{}_value as {}_value;\n",
                        sym.bundled_name, alias, sym.bundled_name, alias
                    ));
                }
            }
            SymbolKind::Variable => {
                let renames = lowering_names(module);
                if let Some(body) = find_function(
                    &functions,
                    &sym.original_name,
                    &renames,
                    &value_bindings,
                    &class_names,
                    &namespace_names,
                    false,
                ) {
                    let callable_body = find_function(
                        &functions,
                        &sym.original_name,
                        &renames,
                        &value_bindings,
                        &class_names,
                        &namespace_names,
                        true,
                    )
                    .unwrap_or_else(|| body.clone());
                    out.push_str(&format!(
                        "\n    /// function-valued variable {} (from ESM)\n    pub fn {}(__args: Vec<w3cos_core::Value>) -> w3cos_core::Value {{\n{}\n        w3cos_core::Value::Undefined\n    }}\n    fn {}_call(__this: w3cos_core::Value, __args: Vec<w3cos_core::Value>) -> w3cos_core::Value {{\n{}\n        w3cos_core::Value::Undefined\n    }}\n",
                        sym.original_name, sym.bundled_name, body, sym.bundled_name, callable_body
                    ));
                    out.push_str(&format!(
                        "    pub fn {}_value() -> w3cos_core::Value {{\n        __w3cos_esm_get_or_init({:?}, || w3cos_core::Value::function(move |__this, __args| {}_call(__this, __args)))\n    }}\n",
                        sym.bundled_name,
                        format!("{}:value", sym.bundled_name),
                        sym.bundled_name,
                    ));
                    let alias = sanitize_ident(&sym.original_name);
                    if alias != sym.bundled_name {
                        out.push_str(&format!(
                            "    pub use self::{} as {};\n    pub use self::{}_value as {}_value;\n",
                            sym.bundled_name, alias, sym.bundled_name, alias
                        ));
                    }
                } else if let Some(target) = find_alias(&functions, &sym.original_name) {
                    if module.lookup(target).is_some() {
                        // Alias to another module-local binding: forward
                        // through the `_get`/`_set` accessor convention so
                        // every referencing module sees one uniform shape
                        // (the target may be a fn, class accessor, or a
                        // variable accessor — resolve the final form).
                        let (target_module, target_bundled, shape) =
                            resolve_namespace_symbol(bundle, &module.path, target, parse_cache, 0);
                        let owner_ns = bundle
                            .modules
                            .iter()
                            .find(|m| m.path == target_module)
                            .map(|m| m.namespace.clone())
                            .unwrap_or_default();
                        let fwd = |suffix: &str| {
                            if target_module == module.path {
                                format!("{target_bundled}{suffix}")
                            } else {
                                format!("super::{owner_ns}::{target_bundled}{suffix}")
                            }
                        };
                        let get_body = match shape {
                            NamespacePropShape::FnValue => format!("{}_value()", fwd("")),
                            NamespacePropShape::ClassValue => format!("{}()", fwd("")),
                            NamespacePropShape::VariableValue => format!("{}_get()", fwd("")),
                            NamespacePropShape::Missing => {
                                "w3cos_core::Value::Undefined".to_string()
                            }
                        };
                        out.push_str(&format!(
                            "\n    /// alias {} → {} (from ESM)\n    pub fn {}_get() -> w3cos_core::Value {{ {get_body} }}\n    pub fn {}_set(value: w3cos_core::Value) -> w3cos_core::Value {{ value }}\n",
                            sym.original_name,
                            target,
                            sym.bundled_name,
                            sym.bundled_name
                        ));
                        let alias = sanitize_ident(&sym.original_name);
                        if alias != sym.bundled_name {
                            out.push_str(&format!(
                                "    pub use self::{}_get as {};\n",
                                sym.bundled_name, alias
                            ));
                        }
                    } else {
                        // Alias to a non-local name (a global like
                        // `undefined`/`window`, or a namespace import):
                        // materialize it as a lazily evaluated variable.
                        let ctx = LowerCtx::new_dynamic_with_bindings(
                            renames.clone(),
                            value_bindings.clone(),
                        )
                        .with_classes(class_names.clone())
                        .with_namespaces(namespace_names.clone());
                        let initializer = ctx.lower_ident(target);
                        out.push_str(&format!(
                            "\n    pub fn {}_get() -> w3cos_core::Value {{ __w3cos_esm_get_or_init({:?}, || {initializer}) }}\n    pub fn {}_set(value: w3cos_core::Value) -> w3cos_core::Value {{ __w3cos_esm_set({:?}, value.clone()); value }}\n",
                            sym.bundled_name,
                            format!("{}:cell", sym.bundled_name),
                            sym.bundled_name,
                            format!("{}:cell", sym.bundled_name),
                        ));
                        let alias = sanitize_ident(&sym.original_name);
                        if alias != sym.bundled_name {
                            out.push_str(&format!(
                                "    pub use self::{}_get as {};\n",
                                sym.bundled_name, alias
                            ));
                        }
                    }
                } else if let Some(initializer) = find_variable(&functions, &sym.original_name) {
                    let ctx = LowerCtx::new_dynamic_with_bindings(
                        renames.clone(),
                        value_bindings.clone(),
                    )
                    .with_classes(class_names.clone())
                    .with_namespaces(namespace_names.clone());
                    let initializer = ctx.lower_expr(initializer);
                    out.push_str(&format!(
                        "\n    pub fn {}_get() -> w3cos_core::Value {{ __w3cos_esm_get_or_init({:?}, || {initializer}) }}\n    pub fn {}_set(value: w3cos_core::Value) -> w3cos_core::Value {{ __w3cos_esm_set({:?}, value.clone()); value }}\n",
                        sym.bundled_name,
                        format!("{}:cell", sym.bundled_name),
                        sym.bundled_name,
                        format!("{}:cell", sym.bundled_name),
                    ));
                    let alias = sanitize_ident(&sym.original_name);
                    if alias != sym.bundled_name {
                        out.push_str(&format!(
                            "    pub use self::{}_get as {};\n",
                            sym.bundled_name, alias
                        ));
                    }
                } else {
                    // No lowerable top-level item: ambient `export var x;`
                    // (type-erasure artifacts), interfaces, enums, or
                    // destructured exports. Their initial value is Undefined
                    // — but a real cell, not a stub: UMD-style modules
                    // (`export var Event; (function (Event) {...})(Event ||
                    // (Event = {}));`) assign the namespace object later, and
                    // the write must stick (see emit_module_init).
                    out.push_str(&format!(
                        "\n    /// variable {} (from ESM) — ambient/unlowered; Undefined until assigned\n    pub fn {}_get() -> w3cos_core::Value {{ __w3cos_esm_get_or_init({:?}, || w3cos_core::Value::Undefined) }}\n    pub fn {}_set(value: w3cos_core::Value) -> w3cos_core::Value {{ __w3cos_esm_set({:?}, value.clone()); value }}\n",
                        sym.original_name,
                        sym.bundled_name,
                        format!("{}:cell", sym.bundled_name),
                        sym.bundled_name,
                        format!("{}:cell", sym.bundled_name),
                    ));
                    let alias = sanitize_ident(&sym.original_name);
                    if alias != sym.bundled_name {
                        out.push_str(&format!(
                            "    pub use self::{}_get as {};\n",
                            sym.bundled_name, alias
                        ));
                    }
                }
            }
        }
    }

    // Module top-level statements (ESM evaluation semantics): every module
    // gets a once-guarded init fn that run_entry calls in bundle order.
    out.push_str(&emit_module_init(
        bundle,
        module,
        &init_items,
        &value_bindings,
        &class_names,
        &namespace_names,
        parse_cache,
    ));

    out.push_str("}\n");
    out
}

/// Emit the per-module init: a once-guarded pair of fns running the module's
/// top-level statements in source order.
///
/// ```text
/// fn m509__init_body() -> w3cos_core::Value {
///     // lowered statements (Value-returning so try-flow `return v;` works)
///     w3cos_core::Value::Undefined
/// }
/// pub fn m509__init() {
///     m509__init_ONCE.with(|once| { if once.replace(true) { return; }
///         m509__init_body();
///     });
/// }
/// ```
///
/// `const/let/var X = init` statements force the existing lazy cell
/// (`{bundled}_get();`) — exactly one evaluation, in source order relative to
/// neighboring statements — instead of lowering a second, divergent copy of
/// the initializer. Declarators with no cell (fn-valued → pure; destructured
/// → ambient stubs) are skipped or lowered for side effects only.
///
/// NOTE (TDZ approximation): with eager inits, normal access is safe, but a
/// circular-import read DURING another module's init may still observe (and
/// permanently cache) Undefined for a not-yet-evaluated cell. Kept as-is.
fn emit_module_init(
    bundle: &EsmBundle,
    module: &BundledModule,
    init_items: &[InitItem],
    value_bindings: &HashSet<String>,
    class_names: &HashSet<String>,
    namespace_names: &HashSet<String>,
    parse_cache: &mut ModuleParseCache,
) -> String {
    let namespace = &module.namespace;
    let renames = lowering_names(module);

    // Classify declarators: force the cell when one exists; lower the
    // statement (for side effects) otherwise.
    let mut chunks: Vec<InitChunk> = Vec::new();
    for item in init_items {
        match item {
            InitItem::Lower(stmt) => chunks.push(InitChunk::Lower(stmt.clone())),
            InitItem::VarDecl(var_decl) => {
                for declarator in &var_decl.decls {
                    match &declarator.name {
                        Pat::Ident(binding) => {
                            let name = binding.id.sym.to_string();
                            let cell = bundle
                                .symbols
                                .iter()
                                .find(|sym| {
                                    sym.module == module.path
                                        && sym.original_name == name
                                        && sym.kind == SymbolKind::Variable
                                })
                                .filter(|sym| variable_has_get_accessors(bundle, sym, parse_cache))
                                .map(|sym| sym.bundled_name.clone());
                            match cell {
                                Some(bundled) => chunks.push(InitChunk::Force(bundled)),
                                None => {
                                    // No cell: fn-valued declarators are pure
                                    // (emitted as plain fn items); anything
                                    // else lowers for side effects.
                                    if !matches!(
                                        declarator.init.as_deref(),
                                        Some(Expr::Arrow(_) | Expr::Fn(_)) | None
                                    ) {
                                        chunks.push(InitChunk::Lower(Stmt::Decl(Decl::Var(
                                            Box::new(VarDecl {
                                                kind: var_decl.kind,
                                                declare: false,
                                                decls: vec![declarator.clone()],
                                                ..var_decl.clone()
                                            }),
                                        ))));
                                    }
                                }
                            }
                        }
                        _ => {
                            // Destructured declarator: lower it (evaluates
                            // the source once into init-body locals), then
                            // write each extracted name back into its cell so
                            // consumers see the real values (the cells' own
                            // initializers cannot see the pattern).
                            chunks.push(InitChunk::Lower(Stmt::Decl(Decl::Var(Box::new(
                                VarDecl {
                                    kind: var_decl.kind,
                                    declare: false,
                                    decls: vec![declarator.clone()],
                                    ..var_decl.clone()
                                },
                            )))));
                            let mut names = Vec::new();
                            collect_pattern_idents(&declarator.name, &mut names);
                            for name in names {
                                if let Some(bundled) = bundle
                                    .symbols
                                    .iter()
                                    .find(|sym| {
                                        sym.module == module.path
                                            && sym.original_name == name
                                            && sym.kind == SymbolKind::Variable
                                    })
                                    .filter(|sym| {
                                        variable_has_get_accessors(bundle, sym, parse_cache)
                                    })
                                    .map(|sym| sym.bundled_name.clone())
                                {
                                    chunks.push(InitChunk::WriteBack(bundled, name));
                                }
                            }
                        }
                    }
                }
            }
        }
    }

    let lowered_stmts: Vec<Stmt> = chunks
        .iter()
        .filter_map(|chunk| match chunk {
            InitChunk::Lower(stmt) => Some(stmt.clone()),
            InitChunk::Force(_) | InitChunk::WriteBack(_, _) => None,
        })
        .collect();

    let mut ctx = LowerCtx::new_dynamic_with_bindings(renames, value_bindings.clone())
        .with_classes(class_names.clone())
        .with_namespaces(namespace_names.clone());
    // Boxed-scope analysis + var hoisting over the lowered statements (top-
    // level closures may capture and assign top-level loop locals).
    ctx.enter_fn_scope(&[], &lowered_stmts);
    let prologue = ctx.hoist_fn_body_vars(&lowered_stmts);

    let mut body = String::new();
    body.push_str(&prologue);
    for chunk in &chunks {
        match chunk {
            InitChunk::Force(bundled) => {
                // Force the lazy cell: exactly one evaluation, in source order.
                body.push_str(&format!("        {bundled}_get();\n"));
            }
            InitChunk::WriteBack(bundled, local) => {
                // Store the destructured local into its cell for consumers.
                body.push_str(&format!(
                    "        {bundled}_set({});\n",
                    ctx.lower_ident(local)
                ));
            }
            InitChunk::Lower(stmt) => {
                body.push_str(&ctx.lower_stmt(stmt));
                body.push('\n');
            }
        }
    }

    let once = format!("{namespace}:init");
    format!(
        "\n    /// Module top-level statements (ESM evaluation), executed once.\n    fn {namespace}__init_body() -> w3cos_core::Value {{\n{body}        w3cos_core::Value::Undefined\n    }}\n    pub fn {namespace}__init() {{\n        if __w3cos_esm_flag_replace({once:?}, true) {{ return; }}\n        {namespace}__init_body();\n    }}\n"
    )
}

/// One emission chunk of a module init, in source order.
enum InitChunk {
    /// Force the lazy cell of a variable symbol (`{bundled}_get();`).
    Force(String),
    /// Store a destructured init-body local into its cell (`{bundled}_set(v);`).
    WriteBack(String, String),
    /// A statement lowered into the init body.
    Lower(Stmt),
}

/// Collect the binding identifiers of a destructuring pattern (for cell
/// write-back after lowering a destructured top-level declaration).
fn collect_pattern_idents(pat: &Pat, names: &mut Vec<String>) {
    match pat {
        Pat::Ident(binding) => names.push(binding.id.sym.to_string()),
        Pat::Array(array) => {
            for elem in array.elems.iter().flatten() {
                collect_pattern_idents(elem, names);
            }
        }
        Pat::Object(object) => {
            for prop in &object.props {
                match prop {
                    ObjectPatProp::Assign(assign) => names.push(assign.key.sym.to_string()),
                    ObjectPatProp::KeyValue(kv) => collect_pattern_idents(&kv.value, names),
                    ObjectPatProp::Rest(rest) => collect_pattern_idents(&rest.arg, names),
                }
            }
        }
        Pat::Assign(assign) => collect_pattern_idents(&assign.left, names),
        Pat::Rest(rest) => collect_pattern_idents(&rest.arg, names),
        _ => {}
    }
}

/// The emitted fn name backing an `import * as ns` namespace object.
fn namespace_fn_name(module: &BundledModule, local: &str) -> String {
    format!("{}_ns_{}", module.namespace, sanitize_ident(local))
}

/// Per-generation cache of module *shape indices*: just the top-level item
/// kinds needed to resolve namespace-export shapes (fn vs alias vs variable).
/// Caching full parsed bodies for every module would hold all SWC ASTs alive
/// at once — that OOMs on Monaco-scale bundles (hundreds of modules).
#[derive(Default)]
struct ModuleParseCache {
    shapes: std::collections::HashMap<std::path::PathBuf, Vec<(String, ItemShape)>>,
}

/// The emission-relevant kind of a top-level item.
#[derive(Clone, PartialEq, Eq)]
enum ItemShape {
    /// Function declaration or function-valued variable (`const f = () =>`).
    Fn,
    /// `export const x = y` alias, pointing at a local name.
    Alias(String),
    /// Any other variable with an initializer.
    Variable,
}

impl ModuleParseCache {
    fn shapes(&mut self, path: &Path) -> &[(String, ItemShape)] {
        self.shapes.entry(path.to_path_buf()).or_insert_with(|| {
            parse_module_functions(path)
                .iter()
                .filter_map(|item| match item {
                    TopLevelItem::Function { name, .. } => Some((name.clone(), ItemShape::Fn)),
                    TopLevelItem::Alias { name, target } => {
                        Some((name.clone(), ItemShape::Alias(target.clone())))
                    }
                    TopLevelItem::Variable { name, .. } => {
                        Some((name.clone(), ItemShape::Variable))
                    }
                    TopLevelItem::Class { .. } => None,
                })
                .collect()
        })
    }
}

/// How a symbol behaves when exposed as a namespace-object property.
enum NamespacePropShape {
    /// Class: the accessor `X()` yields the class object value.
    ClassValue,
    /// Plain fn `X(Vec<Value>)`: wrapped in a `Value::function`.
    FnValue,
    /// Variable with a `{X}_get()` accessor: snapshot-cloned.
    VariableValue,
    /// No emitted item to reference: `Undefined` placeholder.
    Missing,
}

/// Emit the lazy namespace-object accessor for one `import * as ns` binding:
///
/// ```text
/// pub fn m0_ns_monaco() -> Value {
///     // thread-local singleton (built once, reentrancy-guarded)
///     props.insert("editor", super::m1::m1_editor() /* class */);
///     ...
/// }
/// ```
fn emit_namespace_import(
    bundle: &EsmBundle,
    module: &BundledModule,
    ns_import: &crate::esm_resolver::NamespaceImport,
    parse_cache: &mut ModuleParseCache,
) -> String {
    let fn_name = namespace_fn_name(module, &ns_import.local);
    let cell = format!("{fn_name}:namespace");
    let building = format!("{fn_name}:namespace-building");
    let mut out = String::new();
    out.push_str(&format!(
        "\n    /// namespace object for `import * as {}` ({} exports from {})\n",
        ns_import.local,
        ns_import.exports.len(),
        ns_import.target.display()
    ));
    out.push_str(&format!(
        "    pub fn {fn_name}() -> w3cos_core::Value {{\n        if let Some(value) = __w3cos_esm_get({cell:?}) {{ return value; }}\n        if __w3cos_esm_flag_replace({building:?}, true) {{ return w3cos_core::Value::Undefined; }}\n        let mut props = ::std::collections::HashMap::new();\n"
    ));
    for sym in &ns_import.exports {
        let (module_path, bundled, shape) = resolve_namespace_symbol(
            bundle,
            &sym.defining_module,
            &sym.local_name,
            parse_cache,
            0,
        );
        let owner_ns = bundle
            .modules
            .iter()
            .find(|m| m.path == module_path)
            .map(|m| m.namespace.clone());
        let value = match (owner_ns, shape) {
            (Some(owner), NamespacePropShape::ClassValue) => {
                format!("super::{owner}::{bundled}()")
            }
            (Some(owner), NamespacePropShape::FnValue) => {
                format!("super::{owner}::{bundled}_value()")
            }
            (Some(owner), NamespacePropShape::VariableValue) => {
                // Snapshot clone at namespace-build time; live-binding
                // semantics (re-reading the export on each access) are out of
                // scope for this lowering.
                format!("super::{owner}::{bundled}_get()")
            }
            _ => "w3cos_core::Value::Undefined /* export not lowered */".to_string(),
        };
        out.push_str(&format!(
            "            props.insert({:?}.to_string(), {value});\n",
            sym.exported_name
        ));
    }
    out.push_str(&format!(
        "        let value = w3cos_core::Value::object(props);\n        __w3cos_esm_flag_replace({building:?}, false);\n        __w3cos_esm_set({cell:?}, value.clone());\n        value\n    }}\n"
    ));
    // Alias so the local name resolves within this module (same convention as
    // class/function symbols).
    let alias = sanitize_ident(&ns_import.local);
    if alias != fn_name {
        out.push_str(&format!("    pub use self::{fn_name} as {alias};\n"));
    }
    out
}

/// Does a variable symbol's owner emit it in the `{bundled}_get`/`_set`
/// accessor form? True for plain initialized variables and for symbols with
/// no lowerable item at all (ambient `export var x;`, interfaces, enums,
/// destructured exports — all stubbed to `Undefined`).
fn variable_has_get_accessors(
    bundle: &EsmBundle,
    sym: &BundledSymbol,
    parse_cache: &mut ModuleParseCache,
) -> bool {
    debug_assert!(sym.kind == SymbolKind::Variable);
    let _ = bundle;
    let items = parse_cache.shapes(&sym.module);
    let shape = items.iter().find(|(name, _)| name == &sym.original_name);
    match shape {
        None => true, // no item → stubbed accessors
        Some((_, ItemShape::Variable)) => true,
        Some((_, ItemShape::Fn)) => false,
        Some((_, ItemShape::Alias(_))) => true, // aliases are `_get`/`_set` forwarders
    }
}

/// Resolve a namespace-exported symbol to the concrete emitted item backing
/// it, following `export const x = y` alias chains. Returns the defining
/// module, the bundled item name, and how to expose it as a property value.
fn resolve_namespace_symbol(
    bundle: &EsmBundle,
    defining_module: &Path,
    local_name: &str,
    parse_cache: &mut ModuleParseCache,
    depth: usize,
) -> (std::path::PathBuf, String, NamespacePropShape) {
    let missing = || {
        (
            defining_module.to_path_buf(),
            sanitize_ident(local_name),
            NamespacePropShape::Missing,
        )
    };
    if depth > 8 {
        return missing();
    }
    let Some(sym) = bundle
        .symbols
        .iter()
        .find(|s| s.module == defining_module && s.original_name == local_name)
    else {
        return missing();
    };
    match sym.kind {
        SymbolKind::Class => (
            defining_module.to_path_buf(),
            sym.bundled_name.clone(),
            NamespacePropShape::ClassValue,
        ),
        SymbolKind::Function => (
            defining_module.to_path_buf(),
            sym.bundled_name.clone(),
            NamespacePropShape::FnValue,
        ),
        SymbolKind::Variable => {
            let items = parse_cache.shapes(defining_module);
            if items
                .iter()
                .any(|(name, shape)| name == local_name && matches!(shape, ItemShape::Fn))
            {
                // Function-valued variable: emitted as a plain fn.
                return (
                    defining_module.to_path_buf(),
                    sym.bundled_name.clone(),
                    NamespacePropShape::FnValue,
                );
            }
            let alias_target = items.iter().find_map(|(name, shape)| match shape {
                ItemShape::Alias(target) if name == local_name => Some(target.clone()),
                _ => None,
            });
            if let Some(target) = alias_target {
                // `export const x = y` — resolve the aliased local binding.
                if let Some(module) = bundle.modules.iter().find(|m| m.path == defining_module) {
                    if let Some(target_bundled) = module.lookup(&target) {
                        if let Some(target_sym) = bundle
                            .symbols
                            .iter()
                            .find(|s| s.bundled_name == target_bundled)
                        {
                            let target_module = target_sym.module.clone();
                            let target_original = target_sym.original_name.clone();
                            return resolve_namespace_symbol(
                                bundle,
                                &target_module,
                                &target_original,
                                parse_cache,
                                depth + 1,
                            );
                        }
                    }
                }
                return missing();
            }
            if items
                .iter()
                .any(|(name, shape)| name == local_name && matches!(shape, ItemShape::Variable))
            {
                return (
                    defining_module.to_path_buf(),
                    sym.bundled_name.clone(),
                    NamespacePropShape::VariableValue,
                );
            }
            // No top-level item: ambient/unlowered variable — codegen stubs
            // the `{bundled}_get` accessor, so the property can still read it.
            (
                defining_module.to_path_buf(),
                sym.bundled_name.clone(),
                NamespacePropShape::VariableValue,
            )
        }
    }
}

fn lowering_names(module: &BundledModule) -> Vec<(String, String)> {
    // Rename keys are sanitized: the lowering looks names up after
    // `atom_str` (which sanitizes), so `$foo` must map under `_dfoo`.
    let mut names: Vec<(String, String)> = module
        .local_to_bundled
        .iter()
        .map(|(local, bundled)| (sanitize_ident(local), bundled.clone()))
        .collect();
    names.extend(
        module
            .host_imports
            .iter()
            .map(|(local, _)| (sanitize_ident(local), sanitize_ident(local))),
    );
    names.extend(module.literal_imports.iter().map(|(local, _)| {
        let local = sanitize_ident(local);
        (local.clone(), local)
    }));
    names.extend(module.namespace_imports.iter().map(|ns| {
        (
            sanitize_ident(&ns.local),
            namespace_fn_name(module, &ns.local),
        )
    }));
    names
}

/// Parsed function/class from a module source.
#[derive(Clone)]
enum TopLevelItem {
    Function {
        name: String,
        params: Vec<Pat>,
        body: Vec<Stmt>,
    },
    Class {
        name: String,
        class: Box<Class>,
    },
    Variable {
        name: String,
        initializer: Box<Expr>,
    },
    Alias {
        name: String,
        target: String,
    },
}

/// A top-level statement classified for module-init (`m{N}__init`) emission.
/// Function/class/import/export declarations produce no init item — they are
/// already emitted as Rust items.
#[derive(Clone)]
enum InitItem {
    /// A `var`/`let`/`const` declaration statement (possibly exported):
    /// handled per-declarator at emission time (force lazy cells in source
    /// order, or lower for side effects when no cell exists).
    VarDecl(VarDecl),
    /// Any other executable statement (expression statements, if/for/while/
    /// do/switch/try/labeled/block): lowered as-is in the module ctx.
    Lower(Stmt),
}

/// Everything codegen extracts from one module's source.
struct ParsedModuleItems {
    items: Vec<TopLevelItem>,
    init: Vec<InitItem>,
}

fn parse_module_functions(path: &Path) -> Vec<TopLevelItem> {
    parse_module_items(path).items
}

fn parse_module_items(path: &Path) -> ParsedModuleItems {
    let source = match std::fs::read_to_string(path) {
        Ok(s) => s,
        Err(_) => {
            return ParsedModuleItems {
                items: Vec::new(),
                init: Vec::new(),
            };
        }
    };

    let cm: Lrc<SourceMap> = Default::default();
    let fm = cm.new_source_file(Lrc::new(FileName::Real(path.to_path_buf())), source);
    let extension = path.extension().and_then(|extension| extension.to_str());
    let syntax = if matches!(extension, Some("ts" | "tsx")) {
        Syntax::Typescript(TsSyntax {
            tsx: extension == Some("tsx"),
            ..Default::default()
        })
    } else {
        Syntax::Es(EsSyntax {
            decorators: true,
            jsx: extension == Some("jsx"),
            ..Default::default()
        })
    };
    let lexer = Lexer::new(syntax, Default::default(), StringInput::from(&*fm), None);
    let mut parser = Parser::new_from(lexer);
    let module = match parser.parse_module() {
        Ok(m) => m,
        Err(_) => {
            return ParsedModuleItems {
                items: Vec::new(),
                init: Vec::new(),
            };
        }
    };

    let mut items = Vec::new();
    let mut init = Vec::new();
    for item in &module.body {
        match item {
            ModuleItem::ModuleDecl(ModuleDecl::ExportDecl(export)) => {
                collect_decl_items(&export.decl, &mut items);
                // Exported var declarations still force/lower at init time.
                if let Decl::Var(var_decl) = &export.decl {
                    init.push(InitItem::VarDecl(var_decl.as_ref().clone()));
                }
            }
            ModuleItem::ModuleDecl(ModuleDecl::ExportDefaultDecl(default_decl)) => {
                match &default_decl.decl {
                    // `export default class X {}` — recorded under its local name.
                    DefaultDecl::Class(class_expr) => {
                        if let Some(ident) = &class_expr.ident {
                            items.push(TopLevelItem::Class {
                                name: ident.sym.to_string(),
                                class: class_expr.class.clone(),
                            });
                        }
                    }
                    // A named default function is still a normal local declaration.
                    // The resolver records it as the `default` export, so codegen must
                    // retain its body for imports such as `import App from './App'`.
                    DefaultDecl::Fn(function) => {
                        if let Some(ident) = &function.ident {
                            let params = function
                                .function
                                .params
                                .iter()
                                .map(|param| param.pat.clone())
                                .collect();
                            let body = function
                                .function
                                .body
                                .as_ref()
                                .map(|block| block.stmts.clone())
                                .unwrap_or_default();
                            items.push(TopLevelItem::Function {
                                name: ident.sym.to_string(),
                                params,
                                body,
                            });
                        }
                    }
                    _ => {}
                }
            }
            ModuleItem::ModuleDecl(ModuleDecl::ExportDefaultExpr(default_expr)) => {
                // `export default <expr>;` — evaluate for side effects at
                // init time (there is no symbol cell backing it).
                init.push(InitItem::Lower(Stmt::Expr(ExprStmt {
                    span: default_expr.span,
                    expr: default_expr.expr.clone(),
                })));
            }
            ModuleItem::ModuleDecl(_) => {
                // Imports, named re-exports, `export = ...`: no runtime
                // statement to execute.
            }
            ModuleItem::Stmt(Stmt::Decl(decl)) => {
                collect_decl_items(decl, &mut items);
                if let Decl::Var(var_decl) = decl {
                    init.push(InitItem::VarDecl(var_decl.as_ref().clone()));
                }
                // fn/class declarations are items, not init statements.
            }
            ModuleItem::Stmt(stmt) => {
                init.push(InitItem::Lower(stmt.clone()));
            }
        }
    }
    ParsedModuleItems { items, init }
}

fn collect_decl_items(decl: &Decl, items: &mut Vec<TopLevelItem>) {
    match decl {
        Decl::Fn(fn_decl) => {
            let name = fn_decl.ident.sym.to_string();
            let params = fn_decl
                .function
                .params
                .iter()
                .map(|param| param.pat.clone())
                .collect();
            let body = fn_decl
                .function
                .body
                .as_ref()
                .map(|b| b.stmts.clone())
                .unwrap_or_default();
            items.push(TopLevelItem::Function { name, params, body });
        }
        Decl::Class(class_decl) => {
            items.push(TopLevelItem::Class {
                name: class_decl.ident.sym.to_string(),
                class: class_decl.class.clone(),
            });
        }
        Decl::Var(var_decl) => {
            for declarator in &var_decl.decls {
                let Pat::Ident(binding) = &declarator.name else {
                    continue;
                };
                let Some(init) = &declarator.init else {
                    continue;
                };
                let (params, body) = match init.as_ref() {
                    Expr::Arrow(arrow) => {
                        let params = arrow.params.clone();
                        let body = match arrow.body.as_ref() {
                            BlockStmtOrExpr::BlockStmt(block) => block.stmts.clone(),
                            BlockStmtOrExpr::Expr(expr) => vec![Stmt::Return(ReturnStmt {
                                span: arrow.span,
                                arg: Some(expr.clone()),
                            })],
                        };
                        (params, body)
                    }
                    Expr::Fn(function) => {
                        let params = function
                            .function
                            .params
                            .iter()
                            .map(|param| param.pat.clone())
                            .collect();
                        let body = function
                            .function
                            .body
                            .as_ref()
                            .map(|block| block.stmts.clone())
                            .unwrap_or_default();
                        (params, body)
                    }
                    Expr::Ident(target) => {
                        items.push(TopLevelItem::Alias {
                            name: binding.id.sym.to_string(),
                            target: target.sym.to_string(),
                        });
                        continue;
                    }
                    Expr::Cond(condition)
                        if is_typeof_window_test(&condition.test)
                            && matches!(condition.cons.as_ref(), Expr::Ident(_))
                            && matches!(condition.alt.as_ref(), Expr::Ident(_)) =>
                    {
                        // A native W3COS application owns a real window and
                        // therefore follows the browser branch. Selecting the
                        // fallback branch here gave browser libraries SSR
                        // lifecycle timing (notably react-window used
                        // useEffect instead of useLayoutEffect).
                        let Expr::Ident(target) = condition.cons.as_ref() else {
                            unreachable!()
                        };
                        items.push(TopLevelItem::Alias {
                            name: binding.id.sym.to_string(),
                            target: target.sym.to_string(),
                        });
                        continue;
                    }
                    _ => {
                        items.push(TopLevelItem::Variable {
                            name: binding.id.sym.to_string(),
                            initializer: init.clone(),
                        });
                        continue;
                    }
                };
                items.push(TopLevelItem::Function {
                    name: binding.id.sym.to_string(),
                    params,
                    body,
                });
            }
        }
        _ => {}
    }
}

fn find_function(
    items: &[TopLevelItem],
    name: &str,
    renames: &[(String, String)],
    value_bindings: &HashSet<String>,
    class_names: &HashSet<String>,
    namespace_names: &HashSet<String>,
    dynamic_this: bool,
) -> Option<String> {
    for item in items {
        if let TopLevelItem::Function {
            name: fn_name,
            params,
            body,
        } = item
        {
            if fn_name == name {
                let mut ctx =
                    LowerCtx::new_dynamic_with_bindings(renames.to_vec(), value_bindings.clone())
                        .with_classes(class_names.clone())
                        .with_namespaces(namespace_names.clone());
                if dynamic_this {
                    ctx = ctx.with_function_this();
                }
                ctx.bind_patterns(params);
                // Boxed-scope analysis must run before param bindings and the
                // hoisting prologue are emitted (captured+assigned locals —
                // including params — become Rc<RefCell> cells).
                ctx.enter_fn_scope(params, body);
                let bindings = ctx.lower_closure_params(params);
                let prologue = ctx.hoist_fn_body_vars(body);
                return Some(format!("{bindings}{prologue}{}", ctx.lower_stmts(body)));
            }
        }
    }
    None
}

fn find_variable<'a>(items: &'a [TopLevelItem], name: &str) -> Option<&'a Expr> {
    items.iter().find_map(|item| match item {
        TopLevelItem::Variable {
            name: variable_name,
            initializer,
        } if variable_name == name => Some(initializer.as_ref()),
        _ => None,
    })
}

fn find_alias<'a>(items: &'a [TopLevelItem], name: &str) -> Option<&'a str> {
    items.iter().find_map(|item| match item {
        TopLevelItem::Alias {
            name: alias_name,
            target,
        } if alias_name == name => Some(target.as_str()),
        _ => None,
    })
}

fn is_typeof_window_test(expression: &Expr) -> bool {
    match expression {
        Expr::Bin(binary) => {
            is_typeof_window_test(&binary.left) || is_typeof_window_test(&binary.right)
        }
        Expr::Unary(unary) if unary.op == UnaryOp::TypeOf => {
            matches!(unary.arg.as_ref(), Expr::Ident(ident) if ident.sym == *"window")
        }
        Expr::Paren(parenthesized) => is_typeof_window_test(&parenthesized.expr),
        _ => false,
    }
}

fn find_class<'a>(items: &'a [TopLevelItem], class_name: &str) -> Option<&'a Class> {
    items.iter().find_map(|item| match item {
        TopLevelItem::Class { name, class } if name == class_name => Some(class.as_ref()),
        _ => None,
    })
}

/// Local names in this module that resolve to class symbols (own or
/// imported). Used by the lowering to emit `X()` class-accessor calls.
fn module_class_names(bundle: &EsmBundle, module: &BundledModule) -> HashSet<String> {
    let class_symbols: HashSet<&str> = bundle
        .symbols
        .iter()
        .filter(|sym| sym.kind == SymbolKind::Class)
        .map(|sym| sym.bundled_name.as_str())
        .collect();
    module
        .local_to_bundled
        .iter()
        .filter(|(_, bundled)| class_symbols.contains(bundled.as_str()))
        .map(|(local, _)| sanitize_ident(local))
        .collect()
}

/// A method/getter/setter extracted from a class body, ready for emission.
struct ClassMethodDef {
    /// Unique Rust fn name within the module, e.g. `m1_Point__m_move`.
    fn_name: String,
    /// `&str`-compatible install key argument (literal or computed).
    install_key_arg: String,
    /// Install on the prototype object (`__proto`) or the class object.
    install_on_proto: bool,
    params: Vec<Pat>,
    body: Vec<Stmt>,
    scope: ClassScope,
}

fn lower_dynamic_params(params: &[Pat], renames: &[(String, String)]) -> String {
    let mut bindings = String::new();
    for (index, pattern) in params.iter().enumerate() {
        let argument =
            format!("__args.get({index}).cloned().unwrap_or(w3cos_core::Value::Undefined)");
        lower_dynamic_pattern(pattern, &argument, &mut bindings, renames, 8);
    }
    bindings
}

/// Lower destructuring/default/rest parameter patterns for host-style dynamic
/// functions emitted by the upstream native bridge.
///
/// - free fns `{bundled}__m_*` / `{bundled}__g_*` / `{bundled}__s_*` /
///   `{bundled}__s{m,g,s}_*` / `{bundled}__p*_*` for (static, private)
///   methods, getters, setters, each taking `(__this, __args)`;
/// - a raw ctor fn `{bundled}__ctor` returning `__this`;
/// - `{bundled}__build_class()` assembling the callable class object
///   (prototype object, call slot, statics, inheritance links);
/// - `{bundled}__init_statics()` running static fields/blocks (after the
///   class value is published, so statics may reference the class itself);
/// - a thread-local singleton behind `pub fn {bundled}() -> Value`, which is
///   the item other modules `pub use` to reach the class.
fn lower_dynamic_pattern(
    pattern: &Pat,
    source: &str,
    output: &mut String,
    renames: &[(String, String)],
    indent: usize,
) {
    let pad = " ".repeat(indent);
    match pattern {
        Pat::Ident(ident) => {
            output.push_str(&format!(
                "{pad}let mut {} = {source};\n",
                sanitize_ident(&ident.id.sym.to_string())
            ));
        }
        Pat::Object(object) => {
            let excluded = object
                .props
                .iter()
                .filter_map(|property| match property {
                    ObjectPatProp::Assign(assign) => Some(assign.key.sym.to_string()),
                    ObjectPatProp::KeyValue(key_value) => match &key_value.key {
                        PropName::Ident(ident) => Some(ident.sym.to_string()),
                        PropName::Str(value) => {
                            Some(format!("{:?}", value.value).trim_matches('"').to_string())
                        }
                        PropName::Num(value) => Some(value.value.to_string()),
                        _ => None,
                    },
                    ObjectPatProp::Rest(_) => None,
                })
                .map(|key| format!("{key:?}"))
                .collect::<Vec<_>>()
                .join(", ");
            for property in &object.props {
                match property {
                    ObjectPatProp::Assign(assign) => {
                        let name = sanitize_ident(&assign.key.sym.to_string());
                        let value =
                            format!("{source}.get_property({:?})", assign.key.sym.to_string());
                        if let Some(default) = &assign.value {
                            let ctx = LowerCtx::new_dynamic(renames.to_vec());
                            let fallback = ctx.lower_expr(default);
                            output.push_str(&format!(
                                "{pad}let {name} = {{ let value = {value}; if value.is_undefined() {{ {fallback} }} else {{ value }} }};\n"
                            ));
                        } else {
                            output.push_str(&format!("{pad}let {name} = {value};\n"));
                        }
                    }
                    ObjectPatProp::KeyValue(key_value) => {
                        let key = match &key_value.key {
                            PropName::Ident(ident) => ident.sym.to_string(),
                            PropName::Str(value) => {
                                format!("{:?}", value.value).trim_matches('"').to_string()
                            }
                            PropName::Num(value) => value.value.to_string(),
                            _ => continue,
                        };
                        let nested = format!("{source}.get_property({key:?})");
                        lower_dynamic_pattern(&key_value.value, &nested, output, renames, indent);
                    }
                    ObjectPatProp::Rest(rest) => {
                        let rest_source = format!("{source}.object_rest(&[{excluded}])");
                        lower_dynamic_pattern(&rest.arg, &rest_source, output, renames, indent);
                    }
                }
            }
        }
        Pat::Array(array) => {
            for (index, element) in array.elems.iter().enumerate() {
                if let Some(element) = element {
                    let nested = format!("{source}.get_property({:?})", index.to_string());
                    lower_dynamic_pattern(element, &nested, output, renames, indent);
                }
            }
        }
        Pat::Assign(assign) => {
            let ctx = LowerCtx::new_dynamic(renames.to_vec());
            let fallback = ctx.lower_expr(&assign.right);
            let value = format!(
                "{{ let value = {source}; if value.is_undefined() {{ {fallback} }} else {{ value }} }}"
            );
            lower_dynamic_pattern(&assign.left, &value, output, renames, indent);
        }
        Pat::Rest(rest) => lower_dynamic_pattern(&rest.arg, source, output, renames, indent),
        _ => {}
    }
}

/// Emit the full runtime-class pattern for a top-level class declaration.
fn emit_class(
    sym: &BundledSymbol,
    class: Option<&Class>,
    renames: &[(String, String)],
    value_bindings: &HashSet<String>,
    class_names: &HashSet<String>,
    namespace_names: &HashSet<String>,
) -> String {
    let bundled = &sym.bundled_name;
    let class_name = sanitize_ident(&sym.original_name);
    let base_ctx = || {
        LowerCtx::new_dynamic_with_bindings(renames.to_vec(), value_bindings.clone())
            .with_classes(class_names.clone())
            .with_namespaces(namespace_names.clone())
    };
    let parent = class
        .and_then(|class| class.super_class.as_ref())
        .map(|expr| base_ctx().lower_expr(expr));
    let instance_scope = ClassScope {
        class_name: class_name.clone(),
        parent: parent.clone(),
        is_static: false,
    };
    let static_scope = ClassScope {
        class_name: class_name.clone(),
        parent: parent.clone(),
        is_static: true,
    };

    let mut ctor: Option<(Vec<Pat>, Vec<Stmt>)> = None;
    let mut methods: Vec<ClassMethodDef> = Vec::new();
    let mut field_inits: Vec<String> = Vec::new();
    let mut static_inits: Vec<String> = Vec::new();
    let mut computed_index = 0usize;

    if let Some(class) = class {
        for member in &class.body {
            match member {
                ClassMember::Constructor(constructor) => {
                    let params = constructor
                        .params
                        .iter()
                        .filter_map(|param| match param {
                            ParamOrTsParamProp::Param(param) => Some(param.pat.clone()),
                            ParamOrTsParamProp::TsParamProp(_) => None,
                        })
                        .collect();
                    let body = constructor
                        .body
                        .as_ref()
                        .map(|block| block.stmts.clone())
                        .unwrap_or_default();
                    ctor = Some((params, body));
                }
                ClassMember::Method(method) => {
                    let (prefix, tag) = match (method.is_static, method.kind) {
                        (false, MethodKind::Method) => ("", "m"),
                        (false, MethodKind::Getter) => ("__w3cos_getter_", "g"),
                        (false, MethodKind::Setter) => ("__w3cos_setter_", "s"),
                        (true, MethodKind::Method) => ("", "sm"),
                        (true, MethodKind::Getter) => ("__w3cos_getter_", "sg"),
                        (true, MethodKind::Setter) => ("__w3cos_setter_", "ss"),
                    };
                    let name_ctx = base_ctx();
                    let fn_name = match name_ctx.key_literal(&method.key) {
                        Some(name) => format!("{bundled}__{tag}_{}", sanitize_ident(&name)),
                        None => {
                            computed_index += 1;
                            format!("{bundled}__{tag}_c{computed_index}")
                        }
                    };
                    let install_key_arg = name_ctx.key_arg(prefix, &method.key);
                    methods.push(ClassMethodDef {
                        fn_name,
                        install_key_arg,
                        install_on_proto: !method.is_static,
                        params: method
                            .function
                            .params
                            .iter()
                            .map(|param| param.pat.clone())
                            .collect(),
                        body: method
                            .function
                            .body
                            .as_ref()
                            .map(|block| block.stmts.clone())
                            .unwrap_or_default(),
                        scope: if method.is_static {
                            static_scope.clone()
                        } else {
                            instance_scope.clone()
                        },
                    });
                }
                ClassMember::PrivateMethod(method) => {
                    let (prefix, tag) = match (method.is_static, method.kind) {
                        (false, MethodKind::Method) => ("", "pm"),
                        (false, MethodKind::Getter) => ("__w3cos_getter_", "pg"),
                        (false, MethodKind::Setter) => ("__w3cos_setter_", "ps"),
                        (true, MethodKind::Method) => ("", "spm"),
                        (true, MethodKind::Getter) => ("__w3cos_getter_", "spg"),
                        (true, MethodKind::Setter) => ("__w3cos_setter_", "sps"),
                    };
                    let mangled = LowerCtx::mangle_private(&class_name, &method.key);
                    methods.push(ClassMethodDef {
                        fn_name: format!("{bundled}__{tag}_{}", sanitize_ident(&mangled)),
                        install_key_arg: format!("{:?}", format!("{prefix}{mangled}")),
                        install_on_proto: !method.is_static,
                        params: method
                            .function
                            .params
                            .iter()
                            .map(|param| param.pat.clone())
                            .collect(),
                        body: method
                            .function
                            .body
                            .as_ref()
                            .map(|block| block.stmts.clone())
                            .unwrap_or_default(),
                        scope: if method.is_static {
                            static_scope.clone()
                        } else {
                            instance_scope.clone()
                        },
                    });
                }
                ClassMember::ClassProp(prop) => {
                    let scope = if prop.is_static {
                        &static_scope
                    } else {
                        &instance_scope
                    };
                    let ctx = base_ctx().with_class_scope(scope.clone());
                    let value = prop
                        .value
                        .as_ref()
                        .map(|value| ctx.lower_expr(value))
                        .unwrap_or_else(|| "w3cos_core::Value::Undefined".to_string());
                    let key = ctx.key_arg("", &prop.key);
                    let line = format!("w3cos_core::class::define_field(&__this, {key}, {value});");
                    if prop.is_static {
                        static_inits.push(line);
                    } else {
                        field_inits.push(line);
                    }
                }
                ClassMember::PrivateProp(prop) => {
                    let scope = if prop.is_static {
                        &static_scope
                    } else {
                        &instance_scope
                    };
                    let ctx = base_ctx().with_class_scope(scope.clone());
                    let value = prop
                        .value
                        .as_ref()
                        .map(|value| ctx.lower_expr(value))
                        .unwrap_or_else(|| "w3cos_core::Value::Undefined".to_string());
                    let key = format!("{:?}", LowerCtx::mangle_private(&class_name, &prop.key));
                    let line = format!("w3cos_core::class::define_field(&__this, {key}, {value});");
                    if prop.is_static {
                        static_inits.push(line);
                    } else {
                        field_inits.push(line);
                    }
                }
                ClassMember::StaticBlock(block) => {
                    let mut ctx = base_ctx().with_class_scope(static_scope.clone());
                    ctx.enter_fn_scope(&[], &block.body.stmts);
                    let body = ctx.lower_stmts(&block.body.stmts);
                    static_inits.push(format!("{{ {body} }}"));
                }
                // Index signatures, empty members, auto-accessors: not lowered.
                _ => {}
            }
        }
    }

    // TypeScript overloads redeclare a member; the last definition wins.
    let mut seen: HashSet<String> = HashSet::new();
    let mut unique_methods: Vec<&ClassMethodDef> = Vec::new();
    for def in methods.iter().rev() {
        if seen.insert(def.fn_name.clone()) {
            unique_methods.push(def);
        }
    }
    unique_methods.reverse();

    let mut out = String::new();
    out.push_str(&format!(
        "\n    /// class {} (from ESM)\n",
        sym.original_name
    ));

    // Method / getter / setter free functions.
    for def in &unique_methods {
        let mut ctx = base_ctx().with_class_scope(def.scope.clone());
        ctx.bind_patterns(&def.params);
        ctx.enter_fn_scope(&def.params, &def.body);
        let bindings = ctx.lower_closure_params(&def.params);
        let prologue = ctx.hoist_fn_body_vars(&def.body);
        let body = format!("{prologue}{}", ctx.lower_stmts(&def.body));
        out.push_str(&format!(
            "    fn {}(__this: w3cos_core::Value, __args: Vec<w3cos_core::Value>) -> w3cos_core::Value {{\n{bindings}{body}\n        w3cos_core::Value::Undefined\n    }}\n",
            def.fn_name
        ));
    }

    // Raw constructor.
    let fields = field_inits.join("\n        ");
    let ctor_body = match &ctor {
        Some((params, body)) => {
            let mut ctx = base_ctx().with_class_scope(instance_scope.clone());
            ctx.bind_patterns(params);
            ctx.enter_fn_scope(params, body);
            let bindings = ctx.lower_closure_params(params);
            let prologue = ctx.hoist_fn_body_vars(body);
            let body_code = if parent.is_some() {
                // Field initializers run immediately after super(...).
                let mut code = String::new();
                let mut injected = false;
                for stmt in body {
                    code.push_str(&ctx.lower_stmt(stmt));
                    code.push('\n');
                    if !injected && is_super_call_stmt(stmt) {
                        code.push_str("        ");
                        code.push_str(&fields);
                        code.push('\n');
                        injected = true;
                    }
                }
                if !injected {
                    code.push_str("        ");
                    code.push_str(&fields);
                    code.push('\n');
                }
                code
            } else {
                format!("        {fields}\n{}", ctx.lower_stmts(body))
            };
            format!("{bindings}{prologue}{body_code}")
        }
        None if parent.is_some() => {
            // Derived class without a ctor: forward all args to super.
            format!(
                "        w3cos_core::class::super_ctor(&__this, &{}, __args);\n        {fields}\n",
                parent.clone().unwrap_or_default()
            )
        }
        None => format!("        let _ = &__args;\n        {fields}\n"),
    };
    out.push_str(&format!(
        "    fn {bundled}__ctor(__this: w3cos_core::Value, __args: Vec<w3cos_core::Value>) -> w3cos_core::Value {{\n{ctor_body}        __this\n    }}\n"
    ));

    // Class-object builder.
    out.push_str(&format!(
        "    fn {bundled}__build_class() -> w3cos_core::Value {{\n"
    ));
    if let Some(parent) = &parent {
        out.push_str(&format!("        let __parent = {parent};\n"));
    }
    out.push_str(
        "        let __proto = w3cos_core::Value::object(std::collections::HashMap::new());\n",
    );
    for def in unique_methods.iter().filter(|def| def.install_on_proto) {
        out.push_str(&format!(
            "        __proto.set_property({}, w3cos_core::Value::function({}));\n",
            def.install_key_arg, def.fn_name
        ));
    }
    if parent.is_some() {
        out.push_str(
            "        w3cos_core::class::set_prototype_of(&__proto, &__parent.get_property(\"prototype\"));\n",
        );
    }
    out.push_str("        let __ctor_proto = __proto.clone();\n");
    out.push_str(&format!(
        "        let __class = w3cos_core::Value::callable(std::collections::HashMap::new(), move |_this, __args| {{\n            let __instance = w3cos_core::Value::object(std::collections::HashMap::new());\n            w3cos_core::class::set_prototype_of(&__instance, &__ctor_proto);\n            let __ret = {bundled}__ctor(__instance.clone(), __args);\n            if __ret.is_object() {{ __ret }} else {{ __instance }}\n        }});\n"
    ));
    out.push_str("        __proto.set_property(\"constructor\", __class.clone());\n");
    out.push_str("        __class.set_property(\"prototype\", __proto);\n");
    out.push_str(&format!(
        "        __class.set_property(\"__w3cos_ctor\", w3cos_core::Value::function({bundled}__ctor));\n"
    ));
    for def in unique_methods.iter().filter(|def| !def.install_on_proto) {
        out.push_str(&format!(
            "        __class.set_property({}, w3cos_core::Value::function({}));\n",
            def.install_key_arg, def.fn_name
        ));
    }
    if parent.is_some() {
        out.push_str("        w3cos_core::class::set_prototype_of(&__class, &__parent);\n");
    }
    out.push_str("        __class\n    }\n");

    // Static initializers (fields + blocks in source order).
    out.push_str(&format!(
        "    fn {bundled}__init_statics(__class: &w3cos_core::Value) {{\n"
    ));
    if static_inits.is_empty() {
        out.push_str("        let _ = __class;\n");
    } else {
        out.push_str("        let __this = __class.clone();\n");
        for line in &static_inits {
            out.push_str(&format!("        {line}\n"));
        }
    }
    out.push_str("    }\n");

    // Per-thread singleton + accessor (the pub item other modules use).
    let class_key = format!("{bundled}:class");
    let building_key = format!("{bundled}:class-building");
    out.push_str(&format!(
        "    pub fn {bundled}() -> w3cos_core::Value {{\n        if let Some(value) = __w3cos_esm_get({class_key:?}) {{ return value; }}\n        if __w3cos_esm_flag_replace({building_key:?}, true) {{ return w3cos_core::Value::Undefined; }}\n        let value = {bundled}__build_class();\n        __w3cos_esm_flag_replace({building_key:?}, false);\n        __w3cos_esm_set({class_key:?}, value.clone());\n        {bundled}__init_statics(&value);\n        value\n    }}\n"
    ));
    out
}

