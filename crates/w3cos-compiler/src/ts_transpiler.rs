fn pat_to_string(pat: &Pat) -> String {
    match pat {
        Pat::Ident(ident) => ident.sym.to_string(),
        Pat::Array(arr) => {
            let elems: Vec<_> = arr
                .elems
                .iter()
                .map(|e| e.as_ref().map(pat_to_string).unwrap_or("_".into()))
                .collect();
            format!("[{}]", elems.join(", "))
        }
        Pat::Rest(rest) => format!("..{}", pat_to_string(&rest.arg)),
        Pat::Assign(a) => pat_to_string(&a.left),
        _ => "_".into(),
    }
}

fn pat_target_name(target: &AssignTarget) -> Option<String> {
    match target {
        AssignTarget::Simple(SimpleAssignTarget::Ident(ident)) => {
            Some(ident.sym.to_string())
        }
        _ => None,
    }
}

fn is_fn_decl(stmt: &Stmt) -> bool {
    matches!(stmt, Stmt::Decl(Decl::Fn(_)))
}

fn is_main_fn(stmt: &Stmt) -> bool {
    if let Stmt::Decl(Decl::Fn(fn_decl)) = stmt {
        fn_decl.ident.sym.as_ref() == "main"
    } else {
        false
    }
}

fn is_promise_all(member: &MemberExpr) -> bool {
    if let Expr::Ident(obj) = &*member.obj {
        if let MemberProp::Ident(prop) = &member.prop {
            return obj.sym.as_ref() == "Promise" && prop.sym.as_ref() == "all";
        }
    }
    false
}

fn is_promise_race(member: &MemberExpr) -> bool {
    if let Expr::Ident(obj) = &*member.obj {
        if let MemberProp::Ident(prop) = &member.prop {
            return obj.sym.as_ref() == "Promise" && prop.sym.as_ref() == "race";
        }
    }
    false
}

fn stmt_contains_await(stmt: &Stmt) -> bool {
    match stmt {
        Stmt::Expr(e) => expr_contains_await(&e.expr),
        Stmt::Decl(Decl::Var(vd)) => vd
            .decls
            .iter()
            .any(|d| d.init.as_ref().map(|e| expr_contains_await(e)).unwrap_or(false)),
        Stmt::Return(r) => r
            .arg
            .as_ref()
            .map(|e| expr_contains_await(e))
            .unwrap_or(false),
        Stmt::Block(block) => block.stmts.iter().any(stmt_contains_await),
        Stmt::If(if_stmt) => {
            expr_contains_await(&if_stmt.test)
                || stmt_contains_await(&if_stmt.cons)
                || if_stmt
                    .alt
                    .as_ref()
                    .map(|s| stmt_contains_await(s))
                    .unwrap_or(false)
        }
        Stmt::For(f) => {
            f.test
                .as_ref()
                .map(|e| expr_contains_await(e))
                .unwrap_or(false)
                || stmt_contains_await(&f.body)
        }
        Stmt::While(w) => expr_contains_await(&w.test) || stmt_contains_await(&w.body),
        _ => false,
    }
}

fn expr_contains_await(expr: &Expr) -> bool {
    match expr {
        Expr::Await(_) => true,
        Expr::Call(call) => {
            if let Callee::Expr(e) = &call.callee {
                if expr_contains_await(e) {
                    return true;
                }
            }
            call.args.iter().any(|a| expr_contains_await(&a.expr))
        }
        Expr::Bin(bin) => expr_contains_await(&bin.left) || expr_contains_await(&bin.right),
        Expr::Assign(a) => expr_contains_await(&a.right),
        Expr::Member(m) => expr_contains_await(&m.obj),
        Expr::Paren(p) => expr_contains_await(&p.expr),
        Expr::Cond(c) => {
            expr_contains_await(&c.test)
                || expr_contains_await(&c.cons)
                || expr_contains_await(&c.alt)
        }
        _ => false,
    }
}

fn is_console_log(expr: &Expr) -> bool {
    if let Expr::Member(member) = expr {
        if let Expr::Ident(obj) = &*member.obj {
            if let MemberProp::Ident(prop) = &member.prop {
                return obj.sym.as_ref() == "console"
                    && matches!(prop.sym.as_ref(), "log" | "warn" | "error" | "info");
            }
        }
    }
    false
}

struct RangeFor<'a> {
    var: String,
    start: &'a Expr,
    end: &'a Expr,
}

/// Detect `for (let i = START; i < END; i++)` → range pattern
fn detect_range_for(for_stmt: &ForStmt) -> Option<RangeFor<'_>> {
    // init: let i = START
    let (var_name, start) = match &for_stmt.init {
        Some(VarDeclOrExpr::VarDecl(vd)) => {
            let decl = vd.decls.first()?;
            let name = pat_to_string(&decl.name);
            let init = decl.init.as_deref()?;
            (name, init)
        }
        _ => return None,
    };

    // test: i < END
    let end = match &for_stmt.test {
        Some(test) => {
            if let Expr::Bin(bin) = &**test {
                if bin.op == BinaryOp::Lt {
                    if let Expr::Ident(ident) = &*bin.left {
                        if ident.sym.as_ref() == var_name {
                            &*bin.right
                        } else {
                            return None;
                        }
                    } else {
                        return None;
                    }
                } else {
                    return None;
                }
            } else {
                return None;
            }
        }
        None => return None,
    };

    // update: i++ or i += 1
    match &for_stmt.update {
        Some(update) => match &**update {
            Expr::Update(u) => {
                if u.op == UpdateOp::PlusPlus {
                    if let Expr::Ident(ident) = &*u.arg {
                        if ident.sym.as_ref() != var_name {
                            return None;
                        }
                    }
                } else {
                    return None;
                }
            }
            Expr::Assign(a) => {
                if a.op != AssignOp::AddAssign {
                    return None;
                }
            }
            _ => return None,
        },
        None => return None,
    }

    Some(RangeFor {
        var: var_name,
        start,
        end,
    })
}

fn bin_op_to_rust(op: BinaryOp) -> &'static str {
    match op {
        BinaryOp::Add => "+",
        BinaryOp::Sub => "-",
        BinaryOp::Mul => "*",
        BinaryOp::Div => "/",
        BinaryOp::Mod => "%",
        BinaryOp::EqEq | BinaryOp::EqEqEq => "==",
        BinaryOp::NotEq | BinaryOp::NotEqEq => "!=",
        BinaryOp::Lt => "<",
        BinaryOp::LtEq => "<=",
        BinaryOp::Gt => ">",
        BinaryOp::GtEq => ">=",
        BinaryOp::LogicalAnd => "&&",
        BinaryOp::LogicalOr => "||",
        BinaryOp::BitAnd => "&",
        BinaryOp::BitOr => "|",
        BinaryOp::BitXor => "^",
        BinaryOp::LShift => "<<",
        BinaryOp::RShift => ">>",
        BinaryOp::Exp => "/* ** */",
        _ => "/* unknown op */",
    }
}

fn assign_op_to_rust(op: AssignOp) -> &'static str {
    match op {
        AssignOp::Assign => "=",
        AssignOp::AddAssign => "+=",
        AssignOp::SubAssign => "-=",
        AssignOp::MulAssign => "*=",
        AssignOp::DivAssign => "/=",
        AssignOp::ModAssign => "%=",
        AssignOp::BitAndAssign => "&=",
        AssignOp::BitOrAssign => "|=",
        AssignOp::BitXorAssign => "^=",
        AssignOp::LShiftAssign => "<<=",
        AssignOp::RShiftAssign => ">>=",
        _ => "= /* unsupported op */",
    }
}

fn collect_idents_from_expr(expr: &Expr, idents: &mut std::collections::HashSet<String>) {
    match expr {
        Expr::Ident(ident) => {
            idents.insert(ident.sym.to_string());
        }
        Expr::Bin(bin) => {
            collect_idents_from_expr(&bin.left, idents);
            collect_idents_from_expr(&bin.right, idents);
        }
        Expr::Unary(u) => collect_idents_from_expr(&u.arg, idents),
        Expr::Update(u) => collect_idents_from_expr(&u.arg, idents),
        Expr::Assign(a) => {
            if let AssignTarget::Simple(SimpleAssignTarget::Ident(i)) = &a.left {
                idents.insert(i.sym.to_string());
            }
            collect_idents_from_expr(&a.right, idents);
        }
        Expr::Call(call) => {
            if let Callee::Expr(e) = &call.callee {
                collect_idents_from_expr(e, idents);
            }
            for arg in &call.args {
                collect_idents_from_expr(&arg.expr, idents);
            }
        }
        Expr::Member(member) => {
            collect_idents_from_expr(&member.obj, idents);
        }
        Expr::Array(arr) => {
            for elem in arr.elems.iter().flatten() {
                collect_idents_from_expr(&elem.expr, idents);
            }
        }
        Expr::Object(obj) => {
            for prop in &obj.props {
                if let PropOrSpread::Prop(p) = prop {
                    if let Prop::KeyValue(kv) = &**p {
                        collect_idents_from_expr(&kv.value, idents);
                    }
                }
            }
        }
        Expr::Paren(p) => collect_idents_from_expr(&p.expr, idents),
        Expr::Cond(cond) => {
            collect_idents_from_expr(&cond.test, idents);
            collect_idents_from_expr(&cond.cons, idents);
            collect_idents_from_expr(&cond.alt, idents);
        }
        Expr::Tpl(tpl) => {
            for e in &tpl.exprs {
                collect_idents_from_expr(e, idents);
            }
        }
        Expr::Seq(seq) => {
            for e in &seq.exprs {
                collect_idents_from_expr(e, idents);
            }
        }
        Expr::Arrow(_) | Expr::Fn(_) => {
            // Don't recurse into nested closures — they handle their own captures
        }
        _ => {}
    }
}

fn collect_idents_from_stmt(stmt: &Stmt, idents: &mut std::collections::HashSet<String>) {
    match stmt {
        Stmt::Expr(e) => collect_idents_from_expr(&e.expr, idents),
        Stmt::Decl(Decl::Var(vd)) => {
            for decl in &vd.decls {
                if let Some(init) = &decl.init {
                    collect_idents_from_expr(init, idents);
                }
            }
        }
        Stmt::Return(ret) => {
            if let Some(arg) = &ret.arg {
                collect_idents_from_expr(arg, idents);
            }
        }
        Stmt::Block(block) => {
            for s in &block.stmts {
                collect_idents_from_stmt(s, idents);
            }
        }
        Stmt::If(if_stmt) => {
            collect_idents_from_expr(&if_stmt.test, idents);
            collect_idents_from_stmt(&if_stmt.cons, idents);
            if let Some(alt) = &if_stmt.alt {
                collect_idents_from_stmt(alt, idents);
            }
        }
        Stmt::For(for_stmt) => {
            if let Some(VarDeclOrExpr::Expr(e)) = &for_stmt.init {
                collect_idents_from_expr(e, idents);
            }
            if let Some(test) = &for_stmt.test {
                collect_idents_from_expr(test, idents);
            }
            if let Some(update) = &for_stmt.update {
                collect_idents_from_expr(update, idents);
            }
            collect_idents_from_stmt(&for_stmt.body, idents);
        }
        Stmt::While(w) => {
            collect_idents_from_expr(&w.test, idents);
            collect_idents_from_stmt(&w.body, idents);
        }
        _ => {}
    }
}

fn to_snake_case(s: &str) -> String {
    let mut result = String::new();
    for (i, c) in s.chars().enumerate() {
        if c.is_uppercase() {
            if i > 0 {
                result.push('_');
            }
            result.push(c.to_lowercase().next().unwrap());
        } else {
            result.push(c);
        }
    }
    result
}

